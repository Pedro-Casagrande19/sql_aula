WHERE NOME_DISCIPLINA = 'MACHINE LEARNING';

UPDATE OFERTAS
SET MATRICULA_PROFESSOR = 55128
WHERE CODIGO_DISCIPLINA = 12345 AND MATRICULA_PROFESSOR = 55125;

UPDATE ALUNOS_OFERTAS
SET LIMITE_ALUNOS = 70
WHERE CODIGO_OFERTA IN (SELECT o.CODIGO_OFERTA
		FROM OFERTAS o 
		JOIN DISCIPLINAS d ON 
		o.CODIGO_DISCIPLINA = d.CODIGO_DISCIPLINA 
		WHERE d.NOME_DISCIPLINA = 'NATURAL LANGUAGE PROCESSING');

